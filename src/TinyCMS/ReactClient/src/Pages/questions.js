var idCounter = 1;

const getId = () => {
    return "question" + (idCounter++);
}


const convertQuestions = (question) => {

    const type = 'question';
    
    return {
        type,
        title: question.q,
        id: getId(),
        answers: question.a.map(({ val, points = 0 }) => ({ val, points }))
    };
}

const convertMultiple = (item) => {
    return { val: item.a, points: item.point || 0 };
}

const parseCategory = (cat) => {


    var multiple = !cat.questions[0].q;
    if (multiple) {
        return {
            type: 'question-multiple',
            id: getId(),
            title: cat.category,
            answers: cat.questions.map(convertMultiple)
        }
    }
    var children = cat.questions.map(convertQuestions);
    return {
        type: 'form-category',
        id: getId(),
        title: cat.category,
        children
    }
}

export const convertToNodes = () => {
    return questionCategories.map(parseCategory);
}